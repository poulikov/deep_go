func ToLittleEndian[T ~uint16 | ~uint32 | ~uint64](number T) T {
	var (
		res  T
		size = int(unsafe.Sizeof(number))
		p    = unsafe.Pointer(&number)
		p2   = unsafe.Pointer(&res)
	)

	for i := range size / 2 {
		*(*byte)(unsafe.Add(p2, i)) = *(*byte)(unsafe.Add(p, size-1-i))
		*(*byte)(unsafe.Add(p2, size-1-i)) = *(*byte)(unsafe.Add(p, i))
	}

	return res
}

func TestConversion(t *testing.T) {
	tests := map[string]struct {
		number uint32
		result uint32
	}{
		"test case #1": {
			number: 0x00000000,
			result: 0x00000000,
		},
		"test case #2": {
			number: 0xFFFFFFFF,
			result: 0xFFFFFFFF,
		},
		"test case #3": {
			number: 0x00FF00FF,
			result: 0xFF00FF00,
		},
		"test case #4": {
			number: 0x0000FFFF,
			result: 0xFFFF0000,
		},
		"test case #5": {
			number: 0x01020304,
			result: 0x04030201,
		},
	}

	for name, test := range tests {
		t.Run(name, func(t *testing.T) {
			result := ToLittleEndian(test.number)
			assert.Equal(t, test.result, result)
		})
	}
}

